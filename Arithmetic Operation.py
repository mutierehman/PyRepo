##There are various operation performed with python data types( int, float, str, bool)

# 1) Integer Operation (int)
# You can perfrom arithematic operation like addition, Subtraction, Multiplication, Devision


print("*************Integer Operation**************")
a=int(input('Enter Value of a: '))
b=int(input('Enter Value of b: '))

# Addition
add_result=a+b
print('Addition: a+b=',add_result)

#Subtraction
sub_result=a-b
print('Subtraction: a-b=',sub_result)

#Multiplication
mult_result=a*b
print('Multiplication: a*b=',mult_result)

#Division
div_result=a/b
print('Division: a/b=',div_result)

#Integer Division (floor Division)
div_int_result=a//b
print('Integer Division: a//b=',div_int_result)

#Modulus (Remainder)
modu_result=a%b
print('Modulus Remainder: a%b=', modu_result)

#Exponention
pow_result=a**b
print('Exponentiation: a**b=',pow_result)

